# Dibuja un cuadrado en la posición (x, y)
def square(x, y):
    """Dibuja un cuadrado usando el path en (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# Devuelve el índice correspondiente al punto en el tablero
def offset(point):
    """Devuelve el índice correspondiente al punto en el tablero."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    return int(x + y * 20)

# Verifica si un punto es válido en el tablero
def valid(point):
    """Verifica si un punto es válido en el tablero."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# Dibuja el mundo utilizando el camino
def world():
    """Dibuja el mundo usando el path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# Mueve a Pacman y a los fantasmas, actualiza el estado del juego
def move():
    """Mueve a Pacman y a todos los fantasmas."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

# Cambia la dirección de Pacman si la nueva dirección es válida
def change(x, y):
    """Cambia la dirección de Pacman si es válida."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

# Configura el entorno gráfico
